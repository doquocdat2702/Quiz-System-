async function seedQuiz(db, title, description, questions) {
  const code = generateCode();
  const quizResult = await db.query(
    "INSERT INTO quizzes (title, description, code) VALUES ($1, $2, $3) RETURNING id",
    [title, description, code]
  );
  const quizId = quizResult.rows[0].id;

  for (const [question, answers, correct] of questions) {
    await db.query(
      "INSERT INTO questions (quiz_id, question, answers, correct) VALUES ($1, $2, $3, $4)",
      [quizId, question, JSON.stringify(answers), correct]
    );
  }
}

