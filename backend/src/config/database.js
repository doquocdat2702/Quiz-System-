const { Pool } = require("pg");
const bcrypt = require("bcryptjs");
require("dotenv").config();

let pool;

function getDatabaseConfig() {
  const connectionString = process.env.DATABASE_URL;

  if (!connectionString) {
    throw new Error("Missing required environment variable: DATABASE_URL");
  }

  const sslEnabled =
    process.env.DATABASE_SSL === "true" ||
    (process.env.NODE_ENV === "production" && process.env.DATABASE_SSL !== "false");

  return {
    connectionString,
    max: 10,
    ssl: sslEnabled ? { rejectUnauthorized: false } : false,
  };
}

function getPool() {
  if (!pool) {
    pool = new Pool(getDatabaseConfig());
  }

  return pool;
}

function generateCode(length = 6) {
  const chars = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let code = "";
  for (let i = 0; i < length; i++) {
    code += chars[Math.floor(Math.random() * chars.length)];
  }
  return code;
}

async function initDb() {
  const db = getPool();

  await db.query(`
    CREATE TABLE IF NOT EXISTS users (
      id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      name       VARCHAR(100) NOT NULL,
      email      VARCHAR(150) NOT NULL UNIQUE,
      password   VARCHAR(255) NOT NULL,
      created_at TIMESTAMPTZ DEFAULT NOW()
    )
  `);

  // Migration: drop role column/constraint if exists
  await db.query(`ALTER TABLE users DROP CONSTRAINT IF EXISTS users_role_check`);
  await db.query(`ALTER TABLE users DROP COLUMN IF EXISTS role`);

  await db.query(`
    CREATE TABLE IF NOT EXISTS quizzes (
      id          INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      title       VARCHAR(200) NOT NULL,
      description TEXT,
      code        VARCHAR(8) UNIQUE,
      created_at  TIMESTAMPTZ DEFAULT NOW()
    )
  `);

  // Migrations
  await db.query(`ALTER TABLE quizzes ADD COLUMN IF NOT EXISTS code VARCHAR(8) UNIQUE`);
  await db.query(`ALTER TABLE quizzes ADD COLUMN IF NOT EXISTS created_by INTEGER REFERENCES users(id) ON DELETE SET NULL`);

  // Backfill: assign codes to quizzes that don't have one
  const noCode = await db.query("SELECT id FROM quizzes WHERE code IS NULL");
  for (const row of noCode.rows) {
    let code, attempts = 0;
    do {
      code = generateCode();
      const exists = await db.query("SELECT id FROM quizzes WHERE code = $1", [code]);
      if (exists.rows.length === 0) break;
    } while (++attempts < 10);
    await db.query("UPDATE quizzes SET code = $1 WHERE id = $2", [code, row.id]);
  }
  if (noCode.rows.length > 0) console.log(`[DB] Backfilled codes for ${noCode.rows.length} quizzes`);

  // Backfill: assign orphan quizzes (created_by NULL) to first user
  const orphans = await db.query(
    `UPDATE quizzes SET created_by = (SELECT id FROM users ORDER BY id LIMIT 1)
     WHERE created_by IS NULL AND EXISTS (SELECT 1 FROM users)
     RETURNING id`
  );
  if (orphans.rows.length > 0) console.log(`[DB] Assigned ${orphans.rows.length} orphan quizzes`);

  await db.query(`
    CREATE TABLE IF NOT EXISTS questions (
      id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      quiz_id    INTEGER NOT NULL REFERENCES quizzes(id) ON DELETE CASCADE,
      question   TEXT NOT NULL,
      answers    JSONB NOT NULL,
      correct    INTEGER NOT NULL,
      created_at TIMESTAMPTZ DEFAULT NOW()
    )
  `);

  await db.query(`
    CREATE TABLE IF NOT EXISTS quiz_attempts (
      id         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      user_id    INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
      quiz_id    INTEGER NOT NULL REFERENCES quizzes(id) ON DELETE CASCADE,
      score      INTEGER NOT NULL,
      total      INTEGER NOT NULL,
      created_at TIMESTAMPTZ DEFAULT NOW()
    )
  `);

  console.log("[DB] PostgreSQL tables are ready");
  await seedData(db);
  console.log("[DB] Connected to PostgreSQL");
}

